class Solution
{
  public:
    void sortIt(long long arr[], long long n)
    {
        for(int i=0;i<n;i++)if(arr[i]%2)arr[i]=(-1)*arr[i];
       sort(arr,arr+n);
       int i=0;
       while(arr[i]<0){
           arr[i] = (-1)*arr[i];
           i++;
       }
    }
};

